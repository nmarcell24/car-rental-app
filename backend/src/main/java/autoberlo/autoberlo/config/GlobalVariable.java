package autoberlo.autoberlo.config;

/**
 * This class defines global constants for different price categories.
 * <p>
 * These constants represent predefined price categories used throughout
 * the application to classify cars into different pricing tiers such as
 * low, mid, and premium.
 * </p>
 *
 * @author Mandrusz Zsolt, Németh Marcell, Szász Kristóf
 */

public class GlobalVariable {

    /**
     * The price threshold for the low-price category.
     */
    public static final int CATEGORY_LOW_PRICE = 10000;

    /**
     * The price threshold for the mid-price category.
     */
    public static final int CATEGORY_MID_PRICE = 20000;

    /**
     * The price threshold for the premium-price category.
     */
    public static final int CATEGORY_PREMIUM_PRICE = 30000;
}
